package model;

public class CompatibilidadeHardware {
	
	String modelo;
	String slot;
	String encapsulamento;
	public String getModelo() {
		return modelo;
	}
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	public String getSlot() {
		return slot;
	}
	public void setSlot(String slot) {
		this.slot = slot;
	}
	public String getEcapsulamento() {
		return encapsulamento;
	}
	public void setEcapsulamento(String ecapsulamento) {
		this.encapsulamento = ecapsulamento;
	}
	
}
